package at.roesel.oadataprocessor.config;

public class ElasticsearchSettings {

    private String host;
    private int port = 9200;

    private String pathToCaCert;
    private String fingerprint;
    private String user = "elastic";
    private String password;

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getFingerprint() {
        return fingerprint;
    }

    public void setFingerprint(String fingerprint) {
        this.fingerprint = fingerprint;
    }

    public String getPathToCaCert() {
        return pathToCaCert;
    }

    public void setPathToCaCert(String pathToCaCert) {
        this.pathToCaCert = pathToCaCert;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
